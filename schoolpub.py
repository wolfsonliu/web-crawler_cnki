def pubnumbercrawler(collages, year_start, year_end, filename):

    bigsubname = {'A': '基础科学', 'B': '工程科技I期', 'C': '工程科技II期', 'D': '农业科技',
                  'E': '医药卫生科技', 'F': '哲学与人文科学', 'G': '社会科学I期', 'H': '社会科学II期',
                  'I': '信息科技', 'J': '经济与管理科学'}
    pagedatadir = './pagedata'
    filedatadir = './filedata'
    os.makedirs(pagedatadir, exist_ok=True)
    os.makedirs(filedatadir, exist_ok=True)
    driver.switch_to.window(driver.window_handles[0])
    driver.switch_to.default_content()
    driver.get('http://kns.cnki.net/kns/brief/result.aspx?dbprefix=CJFQ')
    driver.maximize_window()
    # 检索按钮


    # 选择来源
    journalsource = driver.find_element_by_id('ddSubmit').find_elements_by_class_name('dd01')[3]
    jsall = journalsource.find_element_by_id('AllmediaBox')
    if jsall.is_selected():
        jsall.click()
    jsbox = journalsource.find_elements_by_tag_name('input')[2:]
    jsbox[3].click()
    jsbox[4].click()
    time.sleep(2)

    # 选择类别
    bigsub_list = ['A', 'B', 'C', 'D', 'E', 'F', 'G', 'H', 'I', 'J']

    clear_btn = driver.find_element_by_id('XuekeNavi_Div').find_elements_by_class_name('btn')[0]
    clear_btn.click()

    pubresult = list()
    for year in range(year_start, year_end + 1):
        # 选择年份
        time.sleep(1)
        while True:
            if driver.find_element_by_name('year_from').get_property('value') == str(year):
                break
            else:
                yearfrom = driver.find_element_by_name('year_from')
                yearfrom.send_keys('{0}'.format(year))
        while True:
            if driver.find_element_by_name('year_to').get_property('value') == str(year):
                break
            else:
                yearto = driver.find_element_by_name('year_to')
                yearto.send_keys('{0}'.format(year))
        time.sleep(4)
        for collage_name in collages:
            # 选择学校
            collage_input = driver.find_element_by_id('au_1_value2')
            driver.execute_script('arguments[0].value = arguments[1]', collage_input, collage_name)
            for bigsub in bigsub_list:
                category = driver.find_element_by_id('XuekeNavi_Div').find_element_by_id(bigsub).find_element_by_id('selectbox')
                category.click()
                time.sleep(1)
                searchbtn = driver.find_element_by_id('btnSearch')
                searchbtn.click()
                time.sleep(4)
                result_number = int(get_search_result_number(driver))

                one = [
                       collage_name, bigsubname[bigsub],
                       driver.find_element_by_name('year_from').get_property('value'),
                       driver.find_element_by_name('year_to').get_property('value'),
                       result_number
                ]
                with open(filename, 'ab') as f:
                   f.write('{0},{1},{2},{3},{4}\n'.format(one[0], one[1], one[2], one[3], one[4]).encode('utf-8'))
                pubresult.append(one)
                category.click()
    return pubresult



